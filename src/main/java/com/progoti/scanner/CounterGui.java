    private void dsDirButtonActionPerformed(ActionEvent evt) {//GEN-FIRST:event_dsDirButtonActionPerformed
        // TODO add your handling code here:
        JFileChooser jFileChooser = new JFileChooser();
        jFileChooser.setDialogTitle("Select Directory");
        jFileChooser.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
        jFileChooser.setCurrentDirectory(new File("."));
        jFileChooser.setAcceptAllFileFilterUsed(false);
        jFileChooser.showOpenDialog(this);
        
        if(jFileChooser.getSelectedFile() != null){
            File selectedDSDir = jFileChooser.getSelectedFile();
            dsDirTextField.setText(selectedDSDir.getAbsolutePath());
            dsDirTextField.setEditable(false);
            Utility.writeToFile("ds:" + selectedDSDir.getAbsolutePath(), true, false);

        }
    }//GEN-LAST:event_dsDirButtonActionPerformed

