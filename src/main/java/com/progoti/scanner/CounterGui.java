    private void resetButtonActionPerformed(ActionEvent evt) {//GEN-FIRST:event_resetButtonActionPerformed
        // TODO add your handling code here:
        String value = "";
        if(dsRadioButton.isSelected()){
            value = Utility.writeToFile("", true, true);
        } else if(kycRadioButton.isSelected()){
            value = Utility.writeToFile("", false, true);
        }
        countTextField.setText(String.valueOf(0));
        lastFileNameTextField.setText(value);
    }//GEN-LAST:event_resetButtonActionPerformed

