    private void addButtonGroup() {
        ButtonGroup buttonGroup = new ButtonGroup();
        buttonGroup.add(dsRadioButton);
        buttonGroup.add(kycRadioButton);
    }

    private void setSrcDir() {
        List<String> dirNameList = Utility.getStringListFromFileName();
        if(dirNameList.size() == 1){
            setValueToTextField(dirNameList.get(0));
        } else if(dirNameList.size() == 2){
            setValueToTextField(dirNameList.get(0));
            setValueToTextField(dirNameList.get(1));
        }
    }
    
    private void setValueToTextField(String value){
        if(value.contains("kyc")){
            value = value.substring(value.indexOf(":")+1).trim();
            kycDirTextField.setText(value);
        } else if(value.contains("ds")){
            value = value.substring(value.indexOf(":")+1).trim();
            dsDirTextField.setText(value);
        }
    }

    private void setActionListenerToRadioButtons() {
        dsRadioButton.addActionListener(new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                lastFileNameTextField.setText(Utility.latestDSFileName);
            }
        });
        kycRadioButton.addActionListener(new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                lastFileNameTextField.setText(Utility.latestKYCFileName);
            }
        });
        
    }
